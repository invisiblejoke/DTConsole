    /**
     * check the name input, calculate cost by medical capacity,
     * and create a new medical service resource base on input,
     * display resource created thereafter.
     * @param evt 
     */
    private void mOkBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mOkBtnActionPerformed
        // TODO add your handling code here:
         
        medName= mNameTF.getText(); 
        
        //calculate resource price base on grade
        medPrice= 100+(medCap/15 *200);
        
        //copy the user input name and filter out all space, symbol,and letter
        String check = medName.replaceAll("[^a-zA-Z]", "");
        
        //check user input name if there is anything other then alphabet 
        if (check.equalsIgnoreCase(medName)==true)
        {                
            //add n show resource
            MedicalServices m = new MedicalServices(medName+"\t", medPrice, medCap);
            df.addResource(m);

            //show user what they created
            mNewIDLabel.setText(m.getResourceID());
            mNewNameLabel.setText(m.getResourceName());
            mNewCostLabel.setText(String.valueOf(m.getCost()));
            mNewPatientLabel.setText(String.valueOf(m.getPatientCapacity()));

            resourceTypePanel.setVisible(false);
            medicalServicesPanel.setVisible(false);
            mConfirm.setVisible(true);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Inputed Name cannot have symbol, space, or number");
        }
    }//GEN-LAST:event_mOkBtnActionPerformed

