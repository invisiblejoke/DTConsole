     /**
     * check the name input, calculate cost by inputed effectiveness/toxicity,
     * and create a new pesticide resource base on input,
     * display resource created thereafter.
     * @param evt 
     */
    private void pOkBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pOkBtnActionPerformed
        // TODO add your handling code here:
        
        pesName =pNameTF.getText();
        
        //calculate resource price base on grade
        pesPrice=500;
        if (pesTox ==null)
            pesPrice += 1000;
        else if (pesTox ==Grade.HIGH)
           pesPrice -=200;
        else if (pesTox ==Grade.MEDIUM)
           pesPrice -=100;
               
        if (pesEff == Grade.MEDIUM)
           pesPrice -= 100;
        else if (pesEff ==Grade.LOW)
           pesPrice -= 200;
       
        //copy the user input name and filter out all space, symbol,and letter
        String check = pesName.replaceAll("[^a-zA-Z]", "");
        
        //check user input name if there is anything other then alphabet 
        if (check.equalsIgnoreCase(pesName)==true)
        {                
            //create and add the resource
            Pesticide p =new Pesticide(pesName+"\t",pesPrice,pesEff,pesTox);
            df.addResource(p);

            //show user what they created
            pNewIDLabel.setText(p.getResourceID());
            pNewNameLabel.setText(p.getResourceName());
            pNewCostLabel.setText(String.valueOf(p.getCost()));
            pNewEffLabel.setText(String.valueOf(p.getEffectiveness()));
            pNewToxLabel.setText(String.valueOf(p.getToxicity()));

            resourceTypePanel.setVisible(false);
            pesticidePanel.setVisible(false);
            pConfirm.setVisible(true);
         }
        else
        {
            JOptionPane.showMessageDialog(this, "Inputed Name cannot have symbol, space, or number");
        }
    }//GEN-LAST:event_pOkBtnActionPerformed

